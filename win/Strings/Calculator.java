class Calculator{
	public static void add(int a,int b){
		System.out.println("Addition of two numbers");
		System.out.println(("1+2")+"="+(a+b));
	}
	public static void sub(int c,int d){
		System.out.println("Subtraction of two numbers");
		System.out.println(("2-1")+"="+(d-c));
	}
	public static void mul(int x,int y){
		System.out.println("Multiplication of two numbers");
		System.out.println(("5*1")+"="+(x*y));
	}
	public static void div(int m,int n){
		System.out.println("Division of two numbers");
		System.out.println(("10/5")+"="+(m/n));
	}
	public static void areaOfRectangle(double height,int width){
		System.out.println("Area of Rectangle:");
		System.out.println(("Area="+(height*width)));
	}
	
}